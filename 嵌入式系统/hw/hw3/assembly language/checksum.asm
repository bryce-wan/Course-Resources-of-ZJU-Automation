ORG 0030H
TABLE: DB 1BH,5H,20H,2FH,26H,0EBH,4FH,11H,0BBH,3AH,17H,23H,0D3H,68H,9H 
	
ORG 0100H
START: 	MOV R2,#0FH	;R2用于记录数组位置
		MOV R0,#30H	;R0用于遍历数组
		MOV DPTR, #0000H
		;MOV R2, #30H
		
LOOP1:	MOV A, R0
		MOVC A,	@A+DPTR 
		MOV @R0, A
		INC  R0
		DJNZ R2, LOOP1
		
RESTET: MOV R2,#0FH
		MOV R0,#30H
		MOV A,#00H
		
LOOP:	ADD A, @R0
		INC R0
		DJNZ R2,LOOP
		
		XRL A,#0FFH	;字节取反
		ADD A,#01H	
		MOV 3FH,A
		
